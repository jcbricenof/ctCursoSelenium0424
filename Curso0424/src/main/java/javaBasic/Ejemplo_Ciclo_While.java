package javaBasic;

public class Ejemplo_Ciclo_While {

	public static void main(String[] args) {
		int a = 1;
		
		//Salir de bucle o del ciclo cuando "a" es mayor que 4
		while (a <= 4) {
			System.out.println("El valor de A: " + a);
			//Incrementar el valor de "a" en 1 para la siguiente interacion
		}

	}

}
