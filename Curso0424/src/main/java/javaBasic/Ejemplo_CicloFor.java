package javaBasic;

public class Ejemplo_CicloFor {

	public static void main(String[] args) {
		int y = 5;
		
		// Bucle for comienza cuando x es igual a 0. X es una variable del ciclo for
		//Corre hasta que x igual o menor que 5
		for (int x = 0; x <= y; x++) {
			System.out.println("El valor de x: " + x);
		}

	}

}
